# RustChain Quickstart Guide

This document provides a quick start guide for users in the RustChain ecosystem. Depending on your role, follow one of the paths below:

## Wallet Setup

1. Download the RustChain wallet from the official repository.
2. Follow the installation instructions provided in the `INSTALLATION.md` file.
3. Create and secure your wallet by following the provided steps.
4. Make sure your wallet is backed up properly.

## Miner Setup

1. Download and install the RustChain miner software from the repository.
2. Configure your miner by editing the configuration file located at `/config/miner_config.json`.
3. Join a mining pool or configure your own.
4. Start the miner by running the following command:

    ```bash
    ./rustchain-miner --config /config/miner_config.json
    ```

5. Monitor the miner's performance using the provided monitoring tool.

## Developer Setup

1. Clone the RustChain repository using Git:

    ```bash
    git clone https://github.com/Scottcjn/rustchain.git
    cd rustchain
    ```

2. Install the necessary dependencies as described in `INSTALLATION.md`.
3. To start development, follow the guidelines outlined in `CONTRIBUTING.md`.

By following these steps, you will have successfully set up your environment for the RustChain project.